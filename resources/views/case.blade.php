                        <h2>KS Endeavor rig-A Chevron project operated by FODE Drilling
Location- Escravos</h2>
                        <div class="desc-text">
                        <p>Project Managers- Mami Marine Limited</p>
<p>Manpower Contractor Owner- GUS Consulting</p>
<p>Project Duration-18 Months</p>
<p>Number of personnel- 105  </p>
 <p>The Mission: to provide and manage a complete crew for the KS Endeavour project.</p>
<p>The rig was docked at a location where there had been several operational challenges due to community issues in the past. After the recruitment campaign, community stakeholders petitioned CNL to give the existing slots to local community workers. CNL for the first time since its operations gave the 3 host communities of Ijaw, Itsekiri and Ilaje community the entire 100% of the national slots. Project went through several tumultuous stages from Community engagement process, conflict resolution at various stages till finally the workers unionized under the Nigerian petroleum and National Gas workers Union (NUPENG). The Strategy
To select the best candidates despite the shortlist originating from the community. Ensuring candidates with red flags due to community or union conflicts were excluded from process was paramount to client. MML consultants were engaged for the entire duration of this process from on boarding, final placement administration, accomodation and crew scheduling with all aspects of crew support, organising trainings as most of the workers had outdated or no certificates to operate, payroll, benefits enrolment and administration, union negotiations, community engagement and conflict resolution. The final phases of the project had MML involved in disengagement process of personnel after closure of project.
During the 18 months of a rather difficult project, through excellent communication, a customer centric organisation and engagement strategy, the rig recorded only 1 strike action for a period of only 12 hours (1 shift). SLAs agreed with the client included zero error on wage and benefits administration and no industrial action. All this were successfully achieved within the duration of the project. MML by being fully involved in all aspects of this project was able to experience a full cycle of all the worst case scenarios on any drilling operation ever recorded in Nigeria making it a reliable partner for manpower solutions.
MML has developed as a business expanding our experience in managing projects in the Niger Delta or hot zones. We’ve demonstrated that our passion, interest and commitment to purpose adds value through high service levels.</p>
